// switch case is used to check one variable with each rather than the if condition

let month = 'March'

switch (month) {
    case "Jan":
        console.log(`you are in the ${month} block`);
        
        break;
    case "FEb":
        console.log(`you are in the ${month} block`);
        
        
        break;
    case "MArch":
        console.log(`you are in the ${month} block`);
        
        
        break;
    case "April":
        console.log(`you are in the ${month} block`);
        
        
        break;
    case "May":
        console.log(`you are in the ${month} block`);
        
        
        break;

    default:
        console.log(`you are in the ${month} block`);
        
        break;
}